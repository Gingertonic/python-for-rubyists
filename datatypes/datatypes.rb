# How to find what type a piece of data is
# thing.class

"snake".class #=> String

true.class #=> TrueClass
false.class #=> FalseClass
nil.class #=> NilClass

42.class #=> Integer
3.14.class #=> Float

:star.class #=> Symbol

["north", "south", "east", "west"].class #=> Array

{ name: "Zelda", age: 2 }.class #=> Hash
{ "name": "Zelda", "age": 2 }.class #=> Hash


